package com.fadineg.trainingproject.second.tasks.class_task_6;

public class Student {
    private String name;
    private String family;
    private String group;
    private int examMath;
    private int examPhysics;
    private int examRussianLanguage;
    private int averageRating;


    public int getExamMath() {
        return examMath;
    }

    public void setExamMath(int examMath) {
        this.examMath = examMath;
    }

    public int getExamPhysics() {
        return examPhysics;
    }

    public void setExamPhysics(int examPhysics) {
        this.examPhysics = examPhysics;
    }

    public int getExamRussianLanguage() {
        return examRussianLanguage;
    }

    public void setExamRussianLanguage(int examRussianLanguage) {
        this.examRussianLanguage = examRussianLanguage;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getFamily() {
        return family;
    }

    public void setFamily(String family) {
        this.family = family;
    }

    public String getGroup() {
        return group;
    }

    public void setGroup(String group) {
        this.group = group;
    }

    public int getAverageRating() {
        return averageRating;
    }

    public void setAverageRating(int averageRating) {
        this.averageRating = averageRating;
    }
}
